import Foundation

public extension Character {
    
    /// EasySDK: Returns A new Line character
    public static var newLineCharacter: Character {
        return "\n"
    }
    
    /// EasySDK: Returns A white space character
    public static var whiteSpaceCharacter: Character {
        return " "
    }
    
    /// EasySDK: Returns a semicolon character
    public static var semiColonCharacter: Character {
        return ";"
    }
    
    /// EasySDK: Returns a colon character
    public static var colonCharacter: Character {
        return ":"
    }
    
    /// EasySDK: Returns a pipe character
    public static var pipeCharacter: Character {
        return "|"
    }
}
